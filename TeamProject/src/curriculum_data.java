
public class curriculum_data {
	public int basic;
	public int specialization;
	public int college;
	public int essential_major;
	public int choice_major;
	
	
	public int getBasic() {
		return basic;
	}
	
	public void setBasic(int basic) {
		this.basic = basic;
	}
	
	public int getSpecialization() {
		return specialization;
	}
	
	public void setSpecialization(int specialization) {
		this.specialization = specialization;
	}
	
	public int getCollege() {
		return college;
	}
	
	public void setCollege(int college) {
		this.college = college;
	}
	
	public int getEssential_major() {
		return essential_major;
	}
	
	public void setEssential_major(int essential_major) {
		this.essential_major = essential_major;
	}
	
	public int getChoice_major() {
		return choice_major;
	}
	
	public void setChoice_major(int choice_major) {
		this.choice_major = choice_major;
	}
	

}
