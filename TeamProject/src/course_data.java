
public class course_data {
	public String classification;
	public int course_id;
	public String course_name;
	public String course_name_eng;
	public String credit;
	public String professor_name;
	public String classroom;
	public String time;
	
	
	public String getClassification() {
		return classification;
	}
	
	public void setClassification(String classification) {
		this.classification = classification;
	}
	
	public int getCourse_id() {
		return course_id;
	}
	
	public void setCourse_id(int course_id) {
		this.course_id = course_id;
	}
	
	public String getCourse_name() {
		return course_name;
	}
	
	public void setCourse_name(String course_name) {
		this.course_name = course_name;
	}
	
	public String getCourse_name_eng() {
		return course_name_eng;
	}
	
	public void setCourse_name_eng(String course_name_eng) {
		this.course_name_eng = course_name_eng;
	}
	
	public String getCredit() {
		return credit;
	}
	
	public void setCredit(String credit) {
		this.credit = credit;
	}
	
	public String getProfessor_name() {
		return professor_name;
	}
	
	public void setProfessor_name(String professor_name) {
		this.professor_name = professor_name;
	}
	
	public String getClassroom() {
		return classroom;
	}
	
	public void setClassroom(String classroom) {
		this.classroom = classroom;
	}
	
	public String getTime() {
		return time;
	}
	
	public void setTime(String time) {
		this.time = time;
	}
	

}
